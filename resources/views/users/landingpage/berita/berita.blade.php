        <div class="card shadow p-3" data-aos="fade-center">
          <div class="card-body">
            <div class="row">
              <div class="col-lg-6">
                <p class="justify">
                  Pada tahun 1964, bertempat di gedung SMP Negeri 2 Palu Jalan Wolter Monginsidi Palu digagaslah berdirinya SGKP berstatus  swasta dengan kepala sekolah Dra. Rahel Bugin. Setahun kemudian, yakni tahun 1965 berdirilah SGKP berstatus negeri. Berdirinya sekolah kepandaian putri ini tidak lepas dari dukungan penuh dari Gubernur Sulawesi Tengah yang kala itu dijabat oleh Anwar Gelar Datuk Baso Majo Nangkuni. Jurusan yang dibuka pada awal berdirinya adalah Menjahit dan Masak-Memasak. Di samping memperoleh dukungan dari pemerintah daerah Sulawesi Tengah, pemerintah pusat pun, yakni Departemen Pendidikan dan Kebudayaan  memberikan dukungan secara hukum dengan menerbitkan SK Pendirian Nomor B3/600/Kej. Tanggal 25 September 1965. Sejak penerbitkan SK Pendirian itu, maka secara resmi di Sulawesi Tengah berdiri satu-satunya sekolah kepandaian putri yang berlokasi di Jalan RA kartini No. 14 Palu.
                </p>
                <p class="justify">
                  Seiring dengan perjalanan waktu dan perkembangan zaman, terjadi beberapa kali pergantian nama dari SGKP ke SMK. Berikut ini adalah proses perubahan nama sekolah ini dari tahun 1965 sampai sekarang, sebagai berikut:
                </p>
                <ol class="justify">
                  <li>Tahun 1964, berdiri SGKP</li>
                  <li>Tahun 1965, berdiri SGKP berstatus negeri</li>
                  <li>Tahun 1970, berganti nama menjadi SKKKA (Sekolah Kejuruan Kesejahteraan Keluarga)</li>
                  <li>Tahun 1979, berubah nama menjadi SMKK (Sekolah Menengah Kesejahteraan Keluarga)</li>
                  <li>Tahun 1997 sampai sekarang, berganti nama menjadi SMK Negeri 1 Palu</li>
                </ol>
                <p class="justify">
                  Sejak berdirinya sekolah ini hingga sekarang telah terjadi beberapa kali pergantian kepemimpinan. Berikut ini adalah nama-nama Kepala SMK Negeri 1 Palu dari tahun 1964 sampai sekarang, sebagai berikut:
                </p>
                <ol class="justify">
                  <li>Tahun 1964, SGKP dipimpin oleh Dra. Rahel Bugin</li>
                  <li>Tahun 1965 sampai 1979, SGKP dipimpin oleh Ny. Kartini Pandan Yotolemba</li>
                  <li>Tahun 1980, sekolah ini dipimpin oleh Dra. Farida Lasahido, sementara Ny. Kartini Pandan dilantik menjadi pengawas di Kanwil P & K Provinsi Sulawesi Tengah</li>
                  <li>Tahun 1988, kepemimpinan diganti oleh Ny. Isah Dumalang Jodjo</li>
                  <li>Tahun 1988, Ny. Isah Dumalang purna tugas, kepemimpinan diganti oleh Dra. Rahmah Hi. Mongki sebagai pejabat sementara</li>
                  <li>Tahun 2000, berdasarkan Keputusan Menteri Pendidikan Nasional Republik Indonesia Nomor: 7877/A.2.I.2/KP/1999 tanggal 3 Desember 1999 dan terhitung tanggal 8 Februari 2000 Dra. Hj. Andi Simpursiah memimpin SMK Negeri 1 Palu</li>
                  <li>Pada tahun 2003, sesuai Surat Keputusan Wali Kota Palu Nomor: 59/82.2/KP/2003 tanggal 6 Agustus 2003, SMK Negeri 1 Palu dipimpin oleh Dra. Hj. Selvi Ladupa</li>
                  <li>Tahun 2013, berdasarkan Surat keputusan Wali Kota Palu Nomor: 821.2/150/BKD/2013, tanggal 25 Januari 2013, tentang pengangkatan Dra. Hj. Misran sebagai kepala SMK Negeri 1 Palu sampai sekarang menggantikan Dra. Hj. Selvi Ladupa yang selanjutnya dilantik menjadi pengawas di lingkungan Dinas Pendidikan dan Kebudayaan Kota Palu.</li>
                </ol>
              </div>
              <div class="col-lg-6">
                <p class="justify">Sekolah ini sejak berdiri tahun 1964 sampai sekarang tetap berorientasi pada sekolah kejuruan yang berfokus pada pengembangan kompetensi kehalian. Sejak berdiri sampai sekarang jurusan  kompetensi yang difokuskan sekolah ini adalah seperti berikut:</p>
                <ol class="justify">
                  <li>Tahun 1965, jurusan yang dibuka adalah Menjahit dan Masak-Memasak</li>
                  <li>Tahun 1970, dibuka satu jurusan lagi adalah Tata Laksana</li>
                  <li>Sejak tahun 1979 seiring dengan perubahan nama menjadi SMKK jurusan pun dikelompokkan: Menjahit ke kelompok Tata Busana, Masak-Memasak ke kelompok Tata Boga, dan ditambah satu jurusan lagi, yakni Tata Graha</li>
                  <li>Tahun 1990, di bawah kepemimpinan Ny. Isah Dumalang Jodjo mmembuka kembali dua jurusan, yakni Tata Rias dan Akomodasi Perhotelan. Dengan demikian, di tahun 1990 SMK Negeri 1 Palu sudah membuka 4 jurusan.</li>
                  <li>Tahun pelajaran 2013/2014 di masa kepemimpinan Dra. Hj. Selvi Ladupa dibuka satu program studi keahlian yaitu Teknologi Informasi dan Komputer dengan kosentrasi kompetensi pada Teknik Komputer dan Jaringan (TKJ). Dengan demikian SMK Negeri 1 Palu memiliki 2 Program Studi Keahlian, yakni (1) Pariwisata yang berfokus pada 4 kompetensi keahlian: (a) Perhotelan, (b) Tata Boga, (c) Tata Kecantikan, (d) Tata Busana, dan (2) Teknik Komputer dan Jaringan.</li>
                  <li>Farmasi Klinis Dan Komunitas (FKK)</li>
                  <li>Asisten Keperwatan (ASKEP)</li>
                </ol>
                <p class="justify">
                  Guna membantu pengembangan pariwista di daerah Sulawesi Tengah, maka satu lagi kompetensi keahlian yang didorong oleh Pemerintah Daerah untuk dibuka di SMK Negeri 1 Palu adalah Usaha Perjalanan Wisata (UPW). Kompetensi keahlian ini dibuka di SMK Negri 1 Palu tahun 2018.
                </p>
